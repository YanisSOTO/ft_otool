#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (n)
	{
		if ((i = *s1 - *s2++) != 0 || !*s1++)
			break ;
		n--;
	}
	return (i);
}
