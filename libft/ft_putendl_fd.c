#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int		i;

	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar_fd(fd, s[i]);
	}
	ft_putchar_fd(fd, '\0');
}
