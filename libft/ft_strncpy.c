#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char	*d;

	d = s1;
	while (*s2 != 0)
	{
		if (!n--)
			return (s1);
		*d++ = *s2++;
	}
	while (n--)
		*d++ = 0;
	return (s1);
}
